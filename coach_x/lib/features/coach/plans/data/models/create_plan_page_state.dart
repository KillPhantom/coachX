/// Create Training Plan Page 状态枚举
///
/// 控制页面在不同创建方式之间的切换
enum CreatePlanPageState {
  /// 初始状态：创建方式选择
  ///
  /// 显示三个创建选项：
  /// - AI 引导创建
  /// - 扫描或粘贴文本
  /// - 手动创建
  initial,

  /// AI 引导创建状态
  ///
  /// 显示参数表单，用户配置训练目标、水平等参数
  aiGuided,

  /// 文本导入状态
  ///
  /// 支持两种方式：
  /// - 扫描图片并 OCR 提取文字
  /// - 直接粘贴文本
  textImport,

  /// 文本导入总结状态
  ///
  /// 显示文本导入解析完成后的统计摘要：
  /// - 总训练天数和动作数
  /// - 复用动作和新建动作数量
  /// - 确认创建新动作模板
  textImportSummary,

  /// AI 流式生成状态
  ///
  /// 显示 AI 流式生成进度页面，展示：
  /// - 4 个生成步骤的实时进度
  /// - 生成统计信息（天数、动作数等）
  /// - 完成后的摘要卡片
  aiStreaming,

  /// 编辑器状态
  ///
  /// 显示训练日编辑器，用户可以：
  /// - 编辑计划名称
  /// - 添加/删除训练日
  /// - 添加/编辑动作和组数
  editing,
}
